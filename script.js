function mod(x){
    return (x % 1 + 1) % 1;
}

function sin_r(angle) {
    return Math.sin(angle * Math.PI / 180);
}

function cos_r(angle) {
    return Math.cos(angle * Math.PI / 180);
}

function tan_r(angle) {
    return Math.tan(angle * Math.PI / 180);
}

function asin_d(x) {
    return Math.asin(x) * 180 / Math.PI;
}

function acos_d(x) {
    return Math.acos(x) * 180 / Math.PI;
}

function atan_d(x) {
    return Math.atan(x) * 180 / Math.PI;
}

function atan2_d(y, x) {
    return Math.atan2(y, x) * 180 / Math.PI;
}

function poly(base, coeffs) {
    return coeffs.reduce((sum, coeff, i) => sum + coeff * base**i, 0);
}

function cnvrt_t(t, utc = new Date()) {
    t %= 1;
    let hour = t * 24;
    let minute = hour % 1 * 60;
    let second = minute % 1 * 60;
    [hour, minute] = [hour, minute].map(t => parseInt(t));
    second = Math.round(second);
    utc.setHours(hour, minute, utc.getSeconds(), 0);
    utc = new Date(utc.getTime() - utc.getTimezoneOffset() * 60000);
    if (second === 60) {
        utc.setSeconds(0);
        utc = new Date(utc.getTime() + 60000);
    } else {
        utc.setSeconds(second);
    }
    return utc;
}

function jd(utc = new Date()) {
    utc = new Date(utc.getTime() + utc.getTimezoneOffset() * 60000);
    let year = utc.getFullYear();
    let month = utc.getMonth() + 1;
    let day = utc.getDate();
    const hour = utc.getHours();
    const minute = utc.getMinutes();
    const second = utc.getSeconds();
    const millisecond = utc.getMilliseconds();
    if (month <= 2) {
        year--;
        month += 12;
    }
    const day_frct = hour / 24 + minute / 1440 + second / 86400 + millisecond / 86400000;
    day += day_frct;
    const A = Math.floor(year / 100);
    const B = 2 - A + Math.floor(A / 4);
    return parseInt(365.25 * (year + 4716)) + parseInt(30.6001 * (month + 1)) + day + B - 1524.5;
}

function astro(utc = new Date()) {
    const JC = (jd(utc) - 2451545) / 36525;
    const U = JC / 100;
    const ε0 = 84381.448 - 4680.93 * U - 1.55 * U**2 + 1999.25 * U**3 - 51.38 * U**4 - 249.67 * U**5 - 39.05 * U**6 + 7.12 * U**7 + 27.87 * U**8 + 5.79 * U**9 + 2.45 * U**10;
    const ε = ε0 / 3600;
    const z = 360.98564736629 * 36525;
    const ν = 280.46061837 + z * JC + 0.000387933 * JC**2 - JC**3 / 38710000;
    return [ε, ν];
}

function angles(λ, β, ξ, lat, long, utc = new Date()) {
    const [ε, ν] = astro(utc);
    const α = atan2_d(sin_r(λ) * cos_r(ε) - tan_r(β) * sin_r(ε), cos_r(λ));
    const δ = asin_d(sin_r(β) * cos_r(ε) + cos_r(β) * sin_r(ε) * sin_r(λ));
    const H = ν - α + long;
    const e = asin_d(sin_r(lat) * sin_r(δ) + cos_r(lat) * cos_r(δ) * cos_r(H));
    const Γ = atan2_d(sin_r(H), cos_r(H) * sin_r(lat) - tan_r(δ) * cos_r(lat));
    const Φ = Γ + 180;
    return [e, Φ, α, δ, H];
}

function sundial(lat, long, utc = new Date()) {
    const JM = (jd(utc) - 2451545) / 365250;

    function LBR(JM, tup) {
        const lst = [];
        for (const arr of tup) {
            let sm = 0;
            for (const row of arr) {
                sm += row[0] * Math.cos(row[1] + row[2] * JM);
            }
            lst.push(sm);
        }
        return lst;
    }

    let L = [L0, L1, L2, L3, L4, L5];
    let B = [B0, B1];
    let R = [R0, R1, R2, R3, R4];
    [L, B, R] = [L, B, R].map(v => poly(JM, LBR(JM, v)) / 1e8);
    [L, B] = [L, B].map(v => v * 180 / Math.PI);
    const θ = L + 180;
    const Δτ = -20.4898 / (3600 * R);
    const λ = θ + Δτ;
    const β = -B;
    const ξ = 8.794 / (3600 * R);
    return angles(λ, β, ξ, lat, long, utc);
}

function timeof(angle, lat, long, utc = new Date()) {
    utc.setHours(0, 0, 0, 0);
    utc = new Date(utc.getTime() - utc.getTimezoneOffset() * 60000);
    const ν = astro(utc)[1];
    let [α0, δ0, m0] = sundial(lat, long, utc).slice(2);
    const [α1, δ1] = sundial(lat, long, new Date(utc.getTime() + 86400000)).slice(2, 4);
    const [α_1, δ_1] = sundial(lat, long, new Date(utc.getTime() - 86400000)).slice(2, 4);
    if (angle === 'h0') {
        angle = -0.8333;
    }
    const H0 = acos_d((sin_r(angle) - sin_r(lat) * sin_r(δ0)) / (cos_r(lat) * cos_r(δ0)));
    m0 *= -1;
    const m1 = m0 - H0;
    const m2 = m0 + H0;

    function intr(x) {
        if (Math.abs(x) > 2) {
            return mod(x);
        }
        return x;
    }

    const a = intr(α0 - α_1);
    const b = intr(α1 - α0);
    const a_ = intr(δ0 - δ_1);
    const b_ = intr(δ1 - δ0);
    const δ = [];
    const H = [];
    const h = [];
    let i = 0;
    for (let m of [m0, m1, m2]) {
        m = mod(m / 360);
        const ν_i = ν + 360.985647 * m;
        const α = α0 + m / 2 * (a + b + m * (b - a));
        δ.push(δ0 + m / 2 * (a_ + b_ + m * (b_ - a_)));
        H.push(ν_i - α + long);
        h.push(asin_d(sin_r(lat) * sin_r(δ[i]) + cos_r(lat) * cos_r(δ[i]) * cos_r(H[i])));
        i++;
    }
    const T = m0 - H[0];
    const R = m1 + (h[1] - angle) / (cos_r(δ[1]) * cos_r(lat) * sin_r(H[1]));
    const S = m2 + (h[2] - angle) / (cos_r(δ[2]) * cos_r(lat) * sin_r(H[2]));
    return [R, S, T].map(m => mod(m / 360));
}

function prayer_times(lat, long, utc = new Date()) {
    const [shurooq, maghrib, dhuhr] = timeof('h0', lat, long, utc);
    const [fajr18, isha18] = timeof(-18, lat, long, utc).slice(0, 2);
    const [fajr15, isha15] = timeof(-15, lat, long, utc).slice(0, 2);
    const dhuhr_angle = sundial(lat, long, cnvrt_t(dhuhr, utc))[0];
    const x = 1;
    const asr_angle = atan2_d(tan_r(dhuhr_angle), 1 + x * tan_r(dhuhr_angle));
    const asr = timeof(asr_angle, lat, long, utc)[1];
    return [fajr18, fajr15, shurooq, dhuhr, asr, maghrib, isha15, isha18];
}

function format_prayer_times(lat, long, utc = new Date()) {
    return prayer_times(lat, long, utc).map(time => cnvrt_t(time, utc)
    .toLocaleTimeString('en-US', { hour: '2-digit', minute: '2-digit', second: '2-digit', hour12: true }).replace('AM', 'ص').replace('PM', 'م')
    );
}

function mod(x){
    return (x % 1 + 1) % 1;
}

function radians(angle){
    return angle * Math.PI / 180;
}

function degrees(x){
    return x * 180 / Math.PI;
}

function sin_r(angle) {
    return Math.sin(radians(angle));
}

function cos_r(angle) {
    return Math.cos(radians(angle));
}

function tan_r(angle) {
    return Math.tan(radians(angle));
}

function asin_d(x) {
    return degrees(Math.asin(x));
}

function acos_d(x) {
    return degrees(Math.acos(x));
}

function atan_d(x) {
    return degrees(Math.atan(x));
}

function atan2_d(y, x) {
    return degrees(Math.atan2(y, x));
}

function poly(base, coeffs) {
    return coeffs.reduce((sum, coeff, i) => sum + coeff * base**i, 0);
}

function cnvrt_t(t, stamp = new Date()) {
    t %= 1;
    let hour = t * 24;
    let minute = hour % 1 * 60;
    let second = minute % 1 * 60;
    [hour, minute] = [hour, minute].map(t => parseInt(t));
    second = Math.round(second);
    stamp.setHours(hour, minute, stamp.getSeconds(), 0);
    stamp = new Date(stamp.getTime() - stamp.getTimezoneOffset() * 60000);
    if (second === 60) {
        stamp.setSeconds(0);
        stamp = new Date(stamp.getTime() + 60000);
    } else {
        stamp.setSeconds(second);
    }
    return stamp;
}

